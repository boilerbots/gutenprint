<H2>Gutenprint Functionality</H2>

<p>Gutenprint is a package of high quality printer drivers for Linux,
BSD, Solaris, Macintosh OS X, IRIX, and other UNIX-alike operating
systems.  In many cases, these drivers rival or exceed the OEM drivers
in quality and functionality.  Our goal is to produce the highest
possible output quality from all supported printers.  To that end, we
have done extensive work on screening algorithms, color generation,
and printer feature utilization.  We are continuing our work in all of
these areas to produce ever higher quality results, particularly on
the ubiquitous, inexpensive inkjet printers that are nonetheless
capable of nearly photographic output quality.</p>

<p>Printing support for <a href="http://www.gimp.org">The GIMP</a> is
only one small part of what Gutenprint does, and indeed, the Print
plugin may eventually be removed from the core Gutenprint
distribution.  Gutenprint printer drivers can be used with all common
UNIX print spooling systems, by means of either <a
href="http://www.cups.org">CUPS</a> or <a
href="http://www.ghostscript.com">Ghostscript</a>.</p>

<p>Here is a list of selected features:</p>

<ul>

<p><li>Support for over 1300 inkjet, dye sublimation, and laser
printers.</li></p>

<p><li>An enhanced Print plug-in for the GIMP.</li></p>

<p><li>A native CUPS driver, providing simple configuration and
operation with CUPS.</li></p>

<p><li>An IJS-based Ghostscript driver that greatly simplifies
installation for Ghostscript versions 6.53 and newer, when used with
Foomatic.</li></p>

<p><li>Extensive output adjustments offer a tremendous range of
adjustment to match color, brightness, and contrast.</li></p>

<p><li>Support for a wide range of printer output resolutions,
permitting you to trade off printing speed against output
quality.</li></p>

<p><li>16-bit precision, producing virtually stepless output.</li></p>

<p><li>Bundled utilities to perform basic maintenance for Epson and
Lexmark printers.</li></p>

<p><li>Extensive documentation for both users and developers.</li></p>

</ul>

